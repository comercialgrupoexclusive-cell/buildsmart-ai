export function normalizarLocalidade(valor: string) {
  return valor
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .trim()
}

export function nomeEstadoPorUf(uf?: string | null) {
  if (!uf) return null
  return UF_NOMES[uf.toUpperCase()] || null
}
